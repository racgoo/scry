/**
 * 두 숫자를 더합니다.
 * @param a 첫 번째 숫자
 * @param b 두 번째 숫자
 * @returns 두 숫자의 합
 */
export function add(a: number, b: number): number {
  return a + b;
}

/**
 * 문자열을 대문자로 변환합니다.
 * @param str 변환할 문자열
 * @returns 대문자로 변환된 문자열
 */
export function toUpperCase(str: string): string {
  return str.toUpperCase();
}
